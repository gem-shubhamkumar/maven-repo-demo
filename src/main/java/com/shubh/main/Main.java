package com.shubh.main;

public class Main {

    public int computeAddition(int num1, int num2) {
        return num1 + num2;
    }

    public int computeDifference(int num1, int num2) {
        return num1 - num2;
    }

    public int computeMultiplication(int num1, int num2) {
        return num1 * num2;
    }

    public int computeDivision(int num1, int num2) {
        return num1 / num2;
    }
}
